public class operadore{
	public static void main(String[] args){
		
		double bonus= 1800;
		bonus += 1000;
		System.out.println(bonus);
		bonus -= 1000;
		System.out.println(bonus);
		bonus *=2;
		System.out.println(bonus);
		bonus /=2;
		System.out.println(bonus);
		bonus %=2;
		System.out.println(bonus);
	}
}